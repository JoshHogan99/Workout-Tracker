import { TemplatesHTML } from "./WorkoutTemplates";
import { useEffect, useState } from "react";

function Workout() {
  const [showHeader, setShowHeader] = useState(false);

  useEffect(() => {
    const handleScroll = () => {
      if (window.scrollY > 36) {
        setShowHeader(true);
      } else {
        setShowHeader(false);
      }
    };

    window.addEventListener("scroll", handleScroll);

    return () => {
      window.removeEventListener("scroll", handleScroll);
    };
  }, []);

  return (
    <div id="workout">
      <div id="header" className={`header ${showHeader ? "show" : ""}`}>
        <p className={`heading ${showHeader ? "show" : ""}`}>Start Workout</p>
      </div>
      <h1>Start Workout</h1>
      <p>Quick Start</p>
      <button>Start an Empty Workout</button>
      <div>
        <h2>Templates</h2>
        <button>+ Template</button>
        <button>
          <FaRegFolderClosed />
        </button>
      </div>
      <TemplatesHTML templates={templates} />
    </div>
  );
}

export default Workout;
